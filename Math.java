public class Math {
    int first, second;

    public int plus(int first,int second){
        return(first+second);
    }

    public int minus(int first,int second){
        return (first-second);
    }

    public int multiply (int first,int second){
        return(first*second);
    }

    public int division (int first,int second){
        return (first/second);
    }
}
